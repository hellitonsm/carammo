export function widthFnFor(scn) {
  const W = scn.widthVariation || 0;
  if (W === 0) return null;
  return (t) => 1 + Math.sin(t * Math.PI * 4) * 0.08 * W + Math.sin(t * Math.PI * 9 + 1.3) * 0.06 * W;
}

export function getScene(id) {
  return SCENES[id];
}
